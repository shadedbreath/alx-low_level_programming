#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>
/**
 * _strcat - used to concatenate two strings
 * @dest: pointer to the destination string
 * @src: pointer to the source string
 *
 * Return: concatenated string
 */

char *_strcat(char *dest, char *src)
{
	return (strcat(dest, src));
}
